'''
5. Crie um conjunto com números inteiros aleatórios e imprima o maior e o
menor número do conjunto.
'''

print("---------- Maior Menor ------------")
aleatorios = {735, 733, 712, 710, 721, 702, 720, 726}

maior_numero = max(aleatorios)
menor_numero = min(aleatorios)


print("Conjunto de números:", aleatorios)
print("Maior número:", maior_numero)
print("Menor número:", menor_numero)
print("-----------------------------------")
